import UIKit

// Flow Control

// if example

let numValue = 5

if numValue < 10 {
    print("Less than 10")
}

// if else example
if numValue > 10 {
    print("Greater than 10")
} else  {
    print("Less than 10")
}

// if else if example
if numValue > 10 {
    print("Greater than 10")
} else if numValue < 10 && numValue == 5 {
    print("Value is 5")
} else {
    print("Default message")
}
